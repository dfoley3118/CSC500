#!/usr/bin/env python3

input_str = input ("please enter a number: ")
num1 = int(input_str)
input_str = input ("please enter a second number: ")
num2 = int(input_str)

num3 = num1 + num2
print(str(num1) + " + " + str(num2) + " = " + str(num3))
num3 = num1 - num2
print(str(num1) + " - " + str(num2) + " = " + str(num3))

